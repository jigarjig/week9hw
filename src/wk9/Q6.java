package wk9;

    public class Q6 {
    public static void main(String[] args) {

        System.out.println("Display right angle triangle using @ :");

        for (int i = 1; i <= 5; i++) {
            for (int a = 1; a <= i; a++) {
                System.out.print("@");
            }
            System.out.println();
        }
    }
    }


